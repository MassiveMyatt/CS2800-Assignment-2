package uk.ac.rhul.cs2800;

/**
 * @author zjac115
 *      This is an enum class for calculator symbols.
 * 
 */
public enum Symbol { // enum created for symbol.
  /**
   * Left bracket symbol.
   */
  LEFT_BRACKET,
  /**
   * Right bracket symbol.
   */
  RIGHT_BRACKET,
  /**
   * Times (multiplication) symbol.
   */
  TIMES,
  /**
   * Divide symbol.
   */
  DIVIDE,
  /**
   * Plus (addition) symbol.
   */
  PLUS,
  /**
   * Minus (subtract) symbol.
   */
  MINUS,
  /**
   * Invalid symbol.
   */
  INVALID,
}
